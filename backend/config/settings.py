# processes. LocMemCache is per-process, so under gunicorn's multiple workers each worker would
# keep its own counter and silently under-throttle. Point REDIS_URL at a shared cache in any
# multi-process deployment (docker-compose already does); single-process `runserver` dev falls
# back to LocMemCache, where per-process counters are fine.
